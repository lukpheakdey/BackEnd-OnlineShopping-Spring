package com.group.backend.demo.authentication.model;


import com.group.backend.demo.authentication.model.Address;
import com.group.backend.demo.authentication.repository.Vendor;

public class VendorForm {

    //user details.
    private String fullName;
    private String phoneNumber;
    private String telephoneNumber;
    private String vendorEmail;
    private String website;
    private String businessType;
    private long userID;

    //address fields.
    private String street;
    private String city;
    private String state;
    private String zip;
    private String country;

    //card information.
    private String cardNo;
    private String cardName;
    private int ccv;
    private String expirationDate;
    private String cardType;

    public Address getAddress() {
        Address address = new Address(street, city, state, zip, country);
        return address;
    }

    public Vendor getVendor(){
        Vendor vendor =  new Vendor(fullName, phoneNumber, telephoneNumber, vendorEmail,  website,userID, businessType);
        return vendor;
    }



    public String getType() {
        if(cardType == null) throw new IllegalArgumentException("type of the card can't be null");
        return cardType;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getTelephoneNumber() {
        return telephoneNumber;
    }

    public void setTelephoneNumber(String telephoneNumber) {
        this.telephoneNumber = telephoneNumber;
    }

    public String getVendorEmail() {
        return vendorEmail;
    }

    public void setVendorEmail(String vendorEmail) {
        this.vendorEmail = vendorEmail;
    }

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }

    public String getBusinessType() {
        return businessType;
    }

    public void setBusinessType(String businessType) {
        this.businessType = businessType;
    }

    public long getUserID() {
        return userID;
    }

    public void setUserID(long userID) {
        this.userID = userID;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getZip() {
        return zip;
    }

    public void setZip(String zip) {
        this.zip = zip;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getCardNo() {
        return cardNo;
    }

    public void setCardNo(String cardNo) {
        this.cardNo = cardNo;
    }

    public String getCardName() {
        return cardName;
    }

    public void setCardName(String cardName) {
        this.cardName = cardName;
    }

    public int getCcv() {
        return ccv;
    }

    public void setCcv(int ccv) {
        this.ccv = ccv;
    }

    public String getExpirationDate() {
        return expirationDate;
    }

    public void setExpirationDate(String expirationDate) {
        this.expirationDate = expirationDate;
    }

    public String getCardType() {
        return cardType;
    }

    public void setCardType(String cardType) {
        this.cardType = cardType;
    }
}
